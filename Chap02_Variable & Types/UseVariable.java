class UseVariable {
    public static void main(String[] args) {
        int num1;
        num1 = 10;
        int num2 = 20;

        int num3 = num1 + num2;
        System.out.println(num1 + " + " + num2 + " + " + num3);

        double db1, db2, result;
        db1 = 1.0000000001;
        db2 = 2.0000000001;
        result = db1 + db2;

        System.out.println(result);
    }
}